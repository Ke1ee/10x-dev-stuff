# For loop will repeat a block of statements

# X is the variable. The number inside brackets is the list. 

for x in [1, 2, 3, 4, 5] :
    print(x)



# Example 2

for x in [2, 4, 6 ,8] :
    print(x)


# Example 3

for x in ['James', 'Bob', 'Jill'] :
    print(x)



# Example 4

# Range function will generate a range of values 

for x in range(5) :
    print(x) 


# Example 5

for x in range(10) :
    print("Hello")


# Example 6 Will only print 1-4 5 will not be displayed.

for x in range(1, 5) :
    print(x) 


#example 7 1 is starting point. 11 is upper limit which will not be displayed. And 2 causes it to skip by 2 

for x in range(1, 11, 2) :
    print(x)